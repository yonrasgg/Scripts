num1 = int(input("Enter a number: "))  # Enter a number: 5
num2 = int(input("Enter another number: "))  # Enter another number: 10

print(num1 > num2)  # Prints: False
print(num1 < num2)  # Prints: True
print(num1 >= num2)  # Prints: False
print(num1 <= num2)  # Prints: True
print(num1 == num2)  # Prints: False
print(num1 != num2)  # Prints: True
print(num1 is num2)  # Prints: False