integer_division = num1 // num2
modulus = num1 % num2

# Results
print('Sum: ', sum_result)
print('Difference: ', difference)
print('Product: ', product)
print('Division: ', division)
print('Integer Division: ', integer_division)
print('Modulus: ', modulus)
