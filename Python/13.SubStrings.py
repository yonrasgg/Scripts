string = "This is an example of Substring (Accessing the Characters of a Text String)"

print(string[0]) # Prints: T
print(string[1]) # Prints: h
print(string[2]) # Prints: i
print(string[3]) # Prints: s
print(string[4]) # Prints:
print(string[5]) # Prints: i
print(string[6]) # Prints: s
print(string[0 : 15]) # Prints: This is an exam