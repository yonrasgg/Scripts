# Define an integer number
num1 = 40
print(type(num1))  # Prints: <class 'int'>
print(num1)  # Prints: 40

# Convert an integer number to float
print(float(num1))  # Prints: 40.0

# Define a floating-point number
num2 = 99.99
print(type(num2))  # Prints: <class 'float'>
print(num2)  # Prints: 99.99

# Convert a floating-point number to int
print(int(num2))  # Prints: 99
