print(string1 * 3) # Prints: Hello, world!Hello, world!Hello, world!
print(string1 * 3 + string2) # Prints: Hello, world!Hello, world!Hello, world!Welcome to Python
print(number1, string1) # Prints: 5 Hello, world!
